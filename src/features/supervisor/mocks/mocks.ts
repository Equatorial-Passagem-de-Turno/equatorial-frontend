// Calcular tempo médio de resolução por table baseado nas ocorrências reais
export function getTempoMedioResolucaoPorMesa() {
  const tablesMap = new Map<string, { total: number; count: number }>();
  
  OCCURRENCES.forEach((ocr) => {
    if (ocr.resolutionTime) {
      const current = tablesMap.get(ocr.table) || { total: 0, count: 0 };
      tablesMap.set(ocr.table, {
        total: current.total + ocr.resolutionTime,
        count: current.count + 1,
      });
    }
  });

  return Array.from(tablesMap.entries()).map(([table, date]) => ({
    table,
    tempoMedio: Math.round(date.total / date.count),
    meta: 60,
  }));
}

// Distribuição de ocorrências por table
export function getDistribuicaoPorMesa() {
  const tablesMap = new Map<string, number>();
  
  OCCURRENCES.forEach((ocr) => {
    tablesMap.set(ocr.table, (tablesMap.get(ocr.table) || 0) + 1);
  });

  return Array.from(tablesMap.entries()).map(([table, total]) => ({
    table,
    total,
  }));
}



