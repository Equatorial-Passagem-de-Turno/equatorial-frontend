type SupervisorHydrationInput = {
  operators?: Operator[];
  occurrences?: Occurrence[];
  activities?: AtividadeRecente[];
};

export function hydrateSupervisorData(input: SupervisorHydrationInput): void {
  if (input.operators && input.operators.length > 0) {
    OPERATORS.splice(0, OPERATORS.length, ...input.operators);
  }

  if (input.occurrences && input.occurrences.length > 0) {
    OCCURRENCES.splice(0, OCCURRENCES.length, ...input.occurrences);
  }

  if (input.activities && input.activities.length > 0) {
    ATIVIDADES_RECENTES.splice(0, ATIVIDADES_RECENTES.length, ...input.activities);
  }
}
